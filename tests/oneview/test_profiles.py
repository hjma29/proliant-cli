def test_profile_alert_display_uses_issue_details_and_plain_ok():
    assert _status_style("OK") == "OK"
    assert _status_style("Critical") == "[red]Critical[/red]"

    messages = _profile_alert_messages({
        "status": "Critical",
        "server_status": "OK",
        "connections": [
            {
                "id": 2,
                "name": "map-connection-2",
                "status": "Disabled",
                "state": "Reserved",
                "port_id": "Mezz 3:2-a",
                "allocated_mbps": 0,
            }
        ],
    })

    assert messages == ["Connection map-connection-2 is Disabled (state: Reserved, port: Mezz 3:2-a, allocated: 0 Mbps)."]


def test_section_banner_styles_title_only():
    banner = _section_banner("Connections")

    assert banner.plain == "########### Connections ###########"
    assert [(span.start, span.end, span.style) for span in banner.spans] == [(12, 23, "bold cyan")]