class FakeClient:
    def __init__(self, collections: dict[str, list[dict]], objects: dict[str, dict] | None = None):
        self._collections = collections
        self._objects = objects or {}

    async def get_all(self, uri: str) -> list[dict]:
        return self._collections.get(uri, [])

    async def get(self, uri: str) -> dict:
        return self._objects.get(uri, {})


